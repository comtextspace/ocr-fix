/**
 * Корректирует текст после OCR-распознавания.
 *
 * Применяемые исправления:
 *  1. Все пробельные символы кроме \n (табы, неразрывные пробелы и др.) → обычный пробел.
 *  2. Перенос слова в конце строки (строка заканчивается на «-»): дефис и \n
 *     удаляются, части слова склеиваются («школь-\nного» → «школьного»).
 *  3. Остальные одиночные \n внутри абзаца → пробел (двойные \n сохраняются).
 *  4. Дефис в роли тире → em dash «—»:
 *     a. «--» (двойной дефис) → «—»
 *     b. Дефис после знака препинания: «,- », «.- », «? - » и т.п. → «знак — »
 *     c. Дефис между пробелами: « - » → « — »
 *  5. Кириллические буквы-двойники в римских цифрах заменяются на латинские
 *     («ХХ» → «XX», «ХIХ» → «XIX»). Срабатывает только на отдельно стоящих токенах,
 *     не являющихся частью кириллического слова.
 *  6. Пробел после точки в аббревиатурах и инициалах: буква + точка + (буква или цифра)
 *     без пробела → добавить пробел («В.И.Ленин» → «В. И. Ленин», «т.1» → «т. 1»).
 *  7. Несколько пробелов подряд → один пробел.
 *
 * @param {string} str - Входной текст после OCR
 * @returns {string} Скорректированный текст
 */


function correct(str) {
  // 1. Все пробельные символы кроме \n → обычный пробел
  str = str.replace(/[^\S\n]/g, ' ')

  // 2. Перенос слова: «-\n» (не перед концом строки/файла и не перед пустой строкой) → «»
  str = str.replace(/-\n(?!($|\n))/g, '')

  // 3. Остальные одиночные \n → пробел (двойные \n сохраняются)
  str = str.replace(/(?<!\n)\n(?!($|\n))/g, ' ')

  // 4a. Двойной дефис → em dash
  //     Только когда вокруг пробелы («слово -- слово») или в начале строки («-- слово»).
  //     Таблицы md (|---|, |--:|) и разделители (---) не затрагиваются.
  str = str.replace(/(^|[ \t])--(?=[ \t])/gm, '$1—')

  // 4b. Дефис после знака препинания (с опциональным пробелом перед ним) → «знак — »
  //     Охватывает: «,- », «.- », «!- », «?- », «»- », «)- » и варианты с пробелом до дефиса
  str = str.replace(/([,.:;!?»)]) ?- /g, '$1 — ')

  // 4c. Одиночный дефис между пробелами → em dash
  str = str.replace(/ - /g, ' — ')

  // 5. Кириллические двойники в XX и XIX → латинские
  //    Только два паттерна: XX (ХХ/ХX/XХ) и XIX (ХIХ и варианты с кириллическими Х и І).
  //    Не срабатывает внутри слов и на более длинных последовательностях (ХХI, ХVIII и др.).
  str = str.replace(
    /(?<![а-яёА-ЯЁa-zA-Z0-9.])[ХX]{2}(?![а-яёА-ЯЁa-zA-Z0-9])/g,
    'XX'
  )
  str = str.replace(
    /(?<![а-яёА-ЯЁa-zA-Z0-9.])[ХX][IІ][ХX](?![а-яёА-ЯЁa-zA-Z0-9])/g,
    'XIX'
  )

  // 6. Аббревиатуры и инициалы: буква + точка + (буква или цифра) без пробела → добавить пробел
  str = str.replace(/([а-яёА-ЯЁa-zA-Z])\.(?=[а-яёА-ЯЁa-zA-Z0-9])/g, '$1. ')

  // 7. Несколько пробелов подряд → один пробел
  str = str.replace(/ {2,}/g, ' ')

  return str
}

/**
 * Регулярное выражение для определения frontmatter в начале файла.
 * Совпадает с блоком «---\n...\n---» строго в начале строки.
 * \n* в конце захватывает все переводы строк после закрывающего ---,
 * чтобы они не попали в тело и не были изменены функцией correct.
 */
const FRONTMATTER_RE = /^---\n[\s\S]*?\n---\n*/

/**
 * Корректирует файл в формате comtext (Markdown с YAML frontmatter).
 *
 * Frontmatter — блок между «---» в самом начале файла — сохраняется без изменений.
 * Следующие блоки в теле файла также сохраняются нетронутыми:
 *  - блоки кода (``` ... ```) и строчный код (`...`)
 *  - блочные формулы ($$ ... $$) и строчные формулы ($...$)
 *  - стихи (строки начинающиеся с |)
 *  - цитаты (строки начинающиеся с >)
 *  - списки (строки начинающиеся с * или цифры с точкой)
 *  - многострочные сноски ([^id]: ... с продолжением через 4 пробела)
 * К остальному тексту применяется функция {@link correct}.
 *
 * @param {string} str - Содержимое comtext-файла
 * @returns {string} Скорректированный текст
 */
function correctComtext(str) {
  const fmMatch = str.match(FRONTMATTER_RE)
  const frontmatter = fmMatch ? fmMatch[0] : ''
  let body = str.slice(frontmatter.length)

  // Механизм защиты блоков: заменяем их плейсхолдерами \x00N\x00,
  // которые correct() не затронет, а после — восстанавливаем.
  const blocks = []

  function protect(text) {
    const id = `\x00${blocks.length}\x00`
    blocks.push(text)
    return id
  }

  // Блочные элементы оборачиваем в \n\n, чтобы правило 3 (склейка строк)
  // не присоединило соседние строки к плейсхолдеру.
  function protectBlock(text) {
    return `\n\n${protect(text)}\n\n`
  }

  // 1. Блоки кода (``` ... ```) — первыми, чтобы содержимое не попало в другие паттерны
  body = body.replace(/^```[^\n]*\n[\s\S]*?\n```[ \t]*$/gm, protectBlock)

  // 2. Строчный код (`...`) — после блоков, чтобы ``` не попало сюда
  body = body.replace(/`[^`\n]+`/g, protect)

  // 3a. Блочные формулы ($$ ... $$)
  body = body.replace(/^\$\$\n[\s\S]*?\n\$\$[ \t]*$/gm, protectBlock)

  // 3b. Строчные формулы ($...$) — после блочных, чтобы $$ не попало сюда
  body = body.replace(/\$(?!\$)[^\n$]+\$/g, protect)

  // 4. Стихи: группы строк начинающихся с |
  body = body.replace(/(^\|[^\n]*\n?)+/gm, protectBlock)

  // 5. Цитаты: корректируем содержимое каждой строки, затем защищаем результат
  body = body.replace(/(^>[^\n]*\n?)+/gm, (block) => {
    const corrected = block.replace(/^(>[ \t]?)([^\n]*)/gm, (_, prefix, content) => prefix + correct(content))
    return protectBlock(corrected)
  })

  // 6. Списки: группы строк начинающихся с * или цифры с точкой
  body = body.replace(/(^(\*|\d+\.)[ \t][^\n]*\n?)+/gm, protectBlock)

  // 7. Многострочные сноски: корректируем содержимое каждой строки,
  //    сохраняя структурные префиксы ([^id]: и 4-пробельный отступ),
  //    затем защищаем результат от повторной обработки.
  body = body.replace(/^\[\^[^\]]+\]:[^\n]*(?:\n(?:$| {4}[^\n]*))*\n?/gm, (block) => {
    const corrected = block.replace(/^(\[\^[^\]]+\]:[ \t]?| {4})([^\n]*)/gm, (_, prefix, content) => prefix + correct(content))
    return protectBlock(corrected)
  })

  // Применяем correct() к тексту с плейсхолдерами
  body = correct(body)

  // Восстанавливаем защищённые блоки
  // eslint-disable-next-line no-control-regex
  body = body.replace(/\x00(\d+)\x00/g, (_, i) => blocks[Number(i)])

  // protectBlock добавляет \n\n с обеих сторон — если блок уже стоял между
  // пустыми строками, образуются 4+ переносов подряд; нормализуем до двух.
  body = body.replace(/\n{3,}/g, '\n\n')

  return frontmatter + body
}

export { correct, correctComtext }
